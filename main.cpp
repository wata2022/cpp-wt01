// iostreamは、C++でコンソールへの入出力（文字の表示など）を行うための標準ライブラリです。
// これをインクルードすることで、std::coutが使えるようになります。
#include <iostream>

// C++プログラムを実行したときに、最初に呼び出される特別な関数がmain関数です。
// プログラムのエントリーポイント（開始点）とも呼ばれます。
int main() {
    // std::coutは、コンソール（ターミナルやコマンドプロンプト）に文字を出力するためのオブジェクトです。
    // << を使って、右側の文字列を左側のstd::coutに送るイメージです。
    // std::endlは、改行して行を終えることを意味します。
    std::cout << "Hello, C++ World!" << std::endl;

    // main関数が正常に終了したことをOS（オペレーティングシステム）に伝えるために、0を返します。
    // 慣習的に、0は「成功」、0以外の数値は「エラー」を意味します。
    return 0;
}